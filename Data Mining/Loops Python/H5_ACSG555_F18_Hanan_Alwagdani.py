# Variable i and sum (initialized to integer zero)

sum=0
i=0

# Constant; Integer value = 100

n=100

# while loop
while i <= n:
    # Sum(i) and saving the Sum in variable sum
    sum= sum + i
    # to increase ONE for value i
    i+=1


# to print sum value
print("The sum when N=100 by using while loop is",sum)

